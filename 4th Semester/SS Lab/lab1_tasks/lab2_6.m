f= [-0.5,1.5];
%round() simply rounds a number according to math rules (-1, 2)
R= round(f)
%fix() will remove the decimal part of the number (0, 1)
Fi= fix(f) 
%ceil() rounds the number to the closest upper whole number (0, 2)
C= ceil(f)
%floor() rounds it to the closest lower whole number (-1, 1)
Fl= floor(f)
